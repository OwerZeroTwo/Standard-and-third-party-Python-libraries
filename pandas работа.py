import numpy as np

# Create an array
arr = np.array([1, 2, 3, 4, 5])

# Print the array
print("Original array:", arr)

# Perform element-wise multiplication
arr_squared = arr ** 2
print("Squared array:", arr_squared)

# Calculate the mean of the array
mean_value = np.mean(arr)
print("Mean value:", mean_value)